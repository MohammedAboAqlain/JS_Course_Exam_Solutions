class TownElement{
    constructor(name, build_year){
        this.name = name;
        this.build_year = build_year;
    }
}

class Park extends TownElement{
    constructor(name, build_year, area, trees){
        super(name, build_year);
        this.area = area;
        this.trees = trees;
    }
    tree_density(){
        return this.trees.length/this.area;
    }
    total_ages(){
        let sum = 0;
        this.trees.forEach(element => {
            sum += element.age;
        });
        return sum;
    }
    avg_age(){
        return this.total_ages()/this.trees.length;
    }
}

class Tree{
    constructor(age){
        this.age = age;
    }
}

class Street extends TownElement{
    constructor(name, build_year, length=null){
        super(name, build_year);
        this.length = length;
    }
    size(){
        if(!this.length){
            return 'normal';
        }
        if(this.length < 0.5){
            return 'tiny';
        } else if(this.length < 1){
            return 'small';
        }else if(this.length < 1.5){
            return 'normal';
        }else if(this.length < 2){
            return 'big';
        }else{
            return 'huge'
        }
    }
}

